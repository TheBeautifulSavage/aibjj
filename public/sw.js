// AIBJJ Service Worker — Offline support + PWA caching
const CACHE_NAME = 'aibjj-v1';
const OFFLINE_URL = '/offline';

// Cache these on install
const PRECACHE_URLS = [
  '/',
  '/dashboard',
  '/coach',
  '/journal',
  '/offline',
];

self.addEventListener('install', (event) => {
  event.waitUntil(
    caches.open(CACHE_NAME).then((cache) => {
      return cache.addAll(PRECACHE_URLS.filter(url => {
        // Only cache if reachable
        return true;
      }));
    }).catch(() => {
      // Silently fail on precache errors
    })
  );
  self.skipWaiting();
});

self.addEventListener('activate', (event) => {
  event.waitUntil(
    caches.keys().then((cacheNames) => {
      return Promise.all(
        cacheNames
          .filter((name) => name !== CACHE_NAME)
          .map((name) => caches.delete(name))
      );
    })
  );
  self.clients.claim();
});

self.addEventListener('fetch', (event) => {
  // Only handle GET requests
  if (event.request.method !== 'GET') return;
  
  // Skip API requests and auth
  const url = new URL(event.request.url);
  if (url.pathname.startsWith('/api/') || url.pathname.startsWith('/_next/')) return;

  event.respondWith(
    fetch(event.request)
      .then((response) => {
        // Cache successful page navigations
        if (response.ok && event.request.mode === 'navigate') {
          const clone = response.clone();
          caches.open(CACHE_NAME).then((cache) => cache.put(event.request, clone));
        }
        return response;
      })
      .catch(() => {
        // Offline fallback
        return caches.match(event.request).then((cached) => {
          if (cached) return cached;
          if (event.request.mode === 'navigate') {
            return caches.match('/') || new Response('Offline - Open AIBJJ when connected', {
              headers: { 'Content-Type': 'text/html' }
            });
          }
        });
      })
  );
});
